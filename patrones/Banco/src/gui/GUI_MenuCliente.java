    public void activar() {
        GUI_FormularioTarjeta ftar = new GUI_FormularioTarjeta();
        txtComp.setText(ftar.cumple);
        if ("Tarjeta Lista".equals(txtComp.getText())) {
            btnTarjeta.setEnabled(true);
        } else {
            btnTarjeta.setEnabled(false);
        }
    }

